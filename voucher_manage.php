                                removeButton.className = 'btn btn-danger';
                                removeButton.textContent = 'Remove';

                                // Add an event listener to remove the activity section when the "Remove" button is clicked
                                removeButton.addEventListener('click', function() {
                                    emptyContainer.removeChild(activitySection);
                                });

                                // Append the inputs and the "Remove" button to the activity section
                                activitySection.appendChild(input1);
                                activitySection.appendChild(input2);
                                activitySection.appendChild(removeButton);

                                // Append the activity section to the empty container
                                emptyContainer.appendChild(activitySection);
                            }

                            // Attach the addActivitySection function to the "Add Activity" button click event
                            const addButtonss = document.getElementById('addButtonss');
                            addButtonss.addEventListener('click', addActivitySection);
