    <h5>Types</h5>
    <p>
    Each country typically has a multitude of categories of visas with various names. The most common types and names of visas include:</p>
    <h6>By purpose</h6>

    <p><strong>Transit visas
</strong></p>
<p>For passing through the country of issue to a destination outside that country. Validity of transit visas are usually limited by short terms such as several hours to ten days depending on the size of the country or the circumstances of a particular transit itinerary.
·         Airside transit visa, required by some countries for passing through their airports even without going through passport control.

·         Crew member, steward or driver visa, issued to persons employed or trained on aircraft, vessels, trains, trucks, buses and any other means of international transportation, or ships fishing in international waters.</p>

<p><strong>Short-stay or visitor visas
</strong></p>
<p>
For short visits to the visited country. Many countries differentiate between different reasons for these visits, such as:
·         Private visa, for private visits by invitation from residents of the visited country.
·         Tourist visa, for a limited period of leisure travel, no business activities allowed.
·         Visa for medical reasons, for undertaking diagnostics or a course of treatment in the visited 
 countries hospitals or other medical facilities.
·         Business visa, for engaging in commerce in the country. These visas generally preclude 
 permanent employment, for which a work visa would be required.
·         Working holiday visa, for individuals traveling between nations offering a working holiday 
 program, allowing young people undertake temporary work while traveling.
·         Athletic or artistic visa, issued to athletes and performing artists (and their supporting staff) 
performing at competitions, concerts, shows and other events.
     ·         Cultural exchange visa, usually issued to athletes and performing artists participating in a 
cultural exchange program.
·         Refugee visa, issued to persons fleeing the dangers of persecution, a war or a natural disaster.
       ·        Pilgrimage visa: this type of visa is mainly issued to those intending to visit religious destinations, as for example in Saudi Arabia or Iran, and to take part in particular religious ceremonies. Such visas can usually be obtained relatively quickly and at low cost; however, those using them are usually permitted to travel only as a group. The best example is Hajj visas  for Saudi Arabia.</p>


<p><strong>Long-stay visas
</strong></p>
<p>
Visas valid for longer but still finite stays:
·         Student visa (F-1 in the U.S.), which allows its holder to study at an institution of higher learning 
in the issuing country. The F-2 visa allows the student's dependents to accompany them in the 
U.S. Research visa, for students doing fieldwork in the host country.

      ·         Temporary worker visa, for approved employment in the host country. These are generally more 
difficult to obtain but valid for longer periods of time than a business visa. Examples of these are 
the United States' H-1B and L-1 visas. Depending on a particular country, the status of temporary 
worker may or may not evolve into the status of permanent resident or to naturalization.

   ·           Journalist visa, which some countries require of people in that occupation when traveling for 
their respective news organizations. Countries which insist on this include Cuba, China, Iran, 
North Korea, Saudi Arabia, the United States (I-visa) and Zimbabwe.

·        Residence visa, granted to people obtaining long-term residence in the host country. In some 
countries, such as New Zealand, long-term residence is a necessary step to obtain the status of 
a permanent resident.
·         Asylum visa, issued to people who have suffered or reasonably fear persecution in their own 
 	country due to their political activities or opinion, or features, or association with a social group; 
or were exiled from their own country.
</p>

<p><strong>Immigrant visas
</strong></p>
<p>
Granted for those intending to immigrate to the issuing country (obtain the status of a permanent resident with a prospect of possible naturalization in the future):
·         Spouse visa or partner visa, granted to the spouse, civil partner or de facto partner of a resident 
or citizen of a given country to enable the couple to settle in that country.
·         Marriage visa, granted for a limited period before intended marriage or civil partnership based 
on a proven relationship with a citizen of the destination country. For example, a German 
woman wishing to marry an American man would obtain a Fiancée Visa (also known as a K-1 
visa) to allow her to enter the United States. A K1 Fiancée Visa is valid for four months from the 
date of its approval. 

·         Pensioner visa (also known as retiree visa or retirement visa), issued by a limited number of 
Countries (Australia, Argentina, Thailand, Panama, etc.), to those who can demonstrate a 
foreign source of income and who do not intend to work in the issuing country. Age limits apply 
in some cases.
</p>

<p><strong>Official visas
</strong></p>
<p>
These are granted to officials doing jobs for their governments, or otherwise representing their countries in the host country, such as the personnel of diplomatic missions.
·         Diplomatic visas are normally only available to bearers of diplomatic passports.
·         Courtesy visas are issued to representatives of foreign governments or international 
organizations who do not qualify for diplomatic status but do merit expedited, courteous 
treatment - an example of this is Australia's Special purpose visa.
</p>

<h6>By method of issue</h6>
<p>Normally visa applications are made at and collected from a consulate, embassy or other diplomatic mission.</p>

<p><strong>On-arrival visas</strong></p>
<p>(Also known as Visa On Arrival, VOA), granted at a port of entry. This is distinct from not requiring a visa at all, as the visitor must still obtain the visa before they can even try to pass through immigration.
·         Almost all countries will consider issuing a visa (or another document to the same effect) on 
arrival to a visitor arriving in unforeseen exceptional circumstances, for example:
       ·        Under provisions of article 35 of the Schengen Visa Code, a visa may be issued at a border in 
situations such as the diversion of a flight causing air passengers in transit to pass through two 
or more airports instead of one. In 2010, Iceland's Eyjafjallajökull volcano erupted, causing 
significant disruption of air travel throughout Europe, and the EU responded by announcing that 
it would issue visas at land borders to stranded travelers.

·     Under section 212(d)(4) of the Immigration and Naturalization Act, visa waivers can be issued to  
     travelers arriving at American ports of entry in emergency situations or under other conditions.

        ·         Certain international airports in Russia have consuls on-duty, who have the power to issue 
visas on the spot.

·          Some countries issue visas on arrival to special categories of travelers, such as seafarers or air crew.

·          Some countries issue them to regular visitors; however, there often are restrictions, for example:
       ·          Belarus issues visas on arrival in Minsk international airport only to nationals of countries where 
  there is no consular representation of Belarus.

       ·         Thailand only issues visas on arrival at certain border checkpoints. The most notable crossing 
where visas on arrival are not issued is the Padang Besar checkpoint for passenger trains 
between Malaysia and Thailand.

       ·         Russia issues visas on arrival for short visits to its Western exclave, Kaliningrad Oblast.</p>



<p><strong>Electronic visas</strong></p>
<p>
An electronic visa (e-Visa or eVisa) is stored in a computer and is linked to the passport number; no label, sticker or stamp is placed in the passport before travel. The application is done over the internet.
·         Antigua and Barbuda issues Electronic Entry Visa (EEV) online to nationals of countries that 
require a visa.
·         Armenia issues electronic visas to certain eligible countries.
·         Australia pioneered electronic visa issuance with the Electronic Travel Authority for tourists, and 
is also issuing the e Visitor for European tourists and businessmen. Recent changes in 
immigration law mean that almost all visas (including those for permanent residency) are 
issued electronically by default unless a label is required (for example to board an aircraft). New 
Zealand citizens however can enter Australia under the Trans Tasman agreement.

·         Azerbaijan issues electronic visas to nationals of 81 countries and stateless persons through its 
ASAN visa system.
·         Bahrain issues electronic visas to nationals of certain eligible countries.
·         Cambodia issues electronic visas to most visitors through their eVisa system.
·         Canada issues electronic visas to former visa-free visitors through their Electronic Travel 
Authorization (eTA) system.
·         Ethiopia issues electronic visas to nationals of countries that are eligible for visa on arrival.
·         Georgia issues electronic visas to nationals of eligible countries
·         India now issues electronic visas (called e-Tourist Visas) to nationals of certain eligible 
countries.
·         Ivory Coast allows visitors to apply online for an electronic visa that if approved can be picked 
up at the Port Bouet Airport in Abidjan.
·         Kenya issues electronic visas to nationals of countries that are eligible for visa on arrival.
·         Kuwait issues electronic visas to nationals of 52 countries via its online portal. Other 
nationalities may apply for an eVisa if they possess a valid GCC residency and work in select 
professions.
·         Myanmar issues electronic visas to nationals of eligible countries.
·         New Zealand is now also issuing some visas electronically.
·         Rwanda issues electronic visas.
·         Saint Kitts and Nevis issues electronic visas to visitors from countries that require a visa for Saint 
Kitts and Nevis.
·         São Tomé and Príncipe issues electronic visas to all passengers through the eVisaST system.
·         Singapore issues electronic visas to certain eligible countries.
·         Sri Lanka issues electronic visas through the Electronic Travel Authorization system to certain 
eligible countries.
·         Turkey issues electronic visas as of April 2013 (certain African countries are eligible only if using 
Turkish Airlines) through the Republic of Turkey Electronic Visa Application System.
·         Uganda issues electronic visas to nationals of countries that are eligible for visa on arrival.
·         Vietnam issues electronic visas to nationals of 40 eligible countries.
These lists are not exhaustive. Some countries may have more detailed classifications of some of these categories reflecting the nuances of their respective geographies, social conditions, economies, international treaties, etc. Others, on the contrary, may combine some types into broader categories.
</p>

<p><strong>Electronic travel authorizations</strong></p>
<p>
A visa is an advance permission to visit a country, introduced for security reasons. Some countries demand an advance authorization obtained over the internet, which are not defined as visas. See also Electronic Travel Authorization.
·         Canada requires all international visitors who do not require a visa except for the United States 
citizens and residents and who are arriving via air to apply for an Electronic Travel Authorization 
(or eTA) before arrival.
·         The United States has an internet system called Electronic System for Travel Authorization (or 
ESTA), but this is a security pre-screening only and does not technically qualify as a visa under 
US immigration law
</p>

<h5>Entry and duration period</h5>
<p>
Visas can also be single-entry which means the visa is canceled as soon as the holder leaves the country; double-entry, or multiple-entry which permits double or multiple entries into the country with the same visa. Countries may also issue re-entry permits that allow temporarily leaving the country without invalidating the visa. Even a business visa will normally not allow the holder to work in the host country without an additional work permit.
Once issued, a visa will typically have to be used within a certain period of time.
With some countries, the validity of a visa is not the same as the authorized period of stay. The visa validity then indicates the time period when entry is permitted into the country. For example, if a visa has been issued to begin on January 1 and to expire on March 30, and the typical authorized period of stay in a country is 90 days, then the 90-day authorized stay starts on the day the passenger enters the country (entrance has to be between January 1 and March 30). Thus, the latest day the traveler could conceivably stay in the issuing country is July 1 (if the traveler entered on March 30). This interpretation of visas is common in the Americas.
With other countries, a person may not stay beyond the period of validity of their visa, which is usually set within the period of validity of their passport. The visa may also limit the total number of days the visitor may spend in the applicable territory within the period of validity. This interpretation of visa periods is common in Europe.
Once in the country, the validity period of a visa or authorized stay can often be extended for a fee at the discretion of immigration authorities. Overstaying a period of authorized stay given by the immigration officers is considered illegal immigration even if the visa validity period isn't over (i.e., for multiple entry visas) and a form of being "out of status" and the offender may be fined, prosecuted, deported, or even blacklisted from entering the country again.
Entering a country without a valid visa or visa exemption may result in detention and removal (deportation or exclusion) from the country. Undertaking activities that are not authorized by the status of entry (for example, working while possessing a non-worker tourist status) can result in the individual being deemed deportable—commonly referred to as an illegal alien. Such violation is not a violation of a visa, despite the common misuse of the phrase, but a violation of status; hence the term "out of status".
Even having a visa does not guarantee entry to the host country. The border crossing authorities make the final determination to allow entry, and may even cancel a visa at the border if the alien cannot demonstrate to their satisfaction that they will abide by the status their visa grants them.
Some countries which do not require visas for short stays may require a long-stay visa for those who intend to apply for a residence permit. For example, the EU does not require a visa of citizens of many countries for stays under 90 days, but its member states require a long-stay visa of such citizens for longer stays.
</p>

<h5>Visa extensions</h5>
<p>
Many countries have a mechanism to allow the holder of a visa to apply to extend a visa. In Denmark, a visa holder can apply to the Danish Immigration Service for a Residence Permit after they have arrived in the country. In the United Kingdom, applications can be made to UK Visas and Immigration.
In certain circumstances, it is not possible for the holder of the visa to do this, either because the country does not have a mechanism to prolong visas or, most likely, because the holder of the visa is using a short stay visa to live in a country.
</p>

<h5>Visa run</h5>
<p>
Some foreign visitors sometimes engage in what is known as a visa run: leaving a country—usually to a neighboring country—for a short period just before the permitted length of stay expires, then returning to the first country to get a new entry stamp in order to extend their stay ("reset the clock"). Despite the name, a visa run is usually done with a passport that can be used for an entry without a visa.
Visa runs are frowned upon by immigration authorities, as such acts may signify that the foreigner wishes to reside permanently and might also work in that country, purposes that visitors are prohibited from engaging in and usually require an immigrant visa or a work visa. Immigration officers may deny re-entry to visitors suspected of engaging in prohibited activities, especially when they have done repeated visa runs and have no evidence of spending reasonable time in their home countries or countries where they have the right to reside and work.
To combat visa run, some countries have limits as to how long visitors can spend in the country without a visa, as well as how much time they may have to stay out before "resetting the clock". For example, Schengen countries impose a maximum limit for visitors of 90 days in a 180-day window. Some countries do not "reset the clock" when a visitor comes back after visiting a neighboring country. For example, the United States does not give visitors a new period of stay when they come back from visiting Canada, Mexico or the Caribbean; instead they are readmitted to the United States for the remaining days granted on their initial entry. Some other countries, e.g. Thailand allows visitors who arrive by land from neighboring countries a shorter length of stay than those who arrive by air.
In some cases, a visa run is necessary to activate new visas or change immigration status of a person, for example, leaving a country and back immediately to activate a newly issued work visa before a person can legally work in that country.
</p>

<h5>Visa refusal</h5>
<p>
In general, an applicant may be refused a visa if he or she does not meet the requirements for admission or entry under that country's immigration laws. More specifically, a visa may be denied or refused when the applicant:
·         has committed fraud, deception or misrepresentation in his or her current application as well 
as in a previous application
·         has obtained a criminal record, has been arrested, or has criminal charges pending
·         is considered to be a threat to national security
·         does not have a good moral character
·         has previous visa/immigration violations (even if the violations didn't happen in the country the 
applicant is seeking a visa for)
·         had their previous visa application(s) or application for immigration benefits refused and 
cannot prove that the reasons for the previous refusals no longer exist or are not applicable any 
more (even if the refusals didn't previously happen in the country the applicant is seeking a visa 
for)
·         cannot prove to have strong ties to their current country of nationality or residence (for those 
who are applying for temporary or non-immigrant visas)
·         intends to reside or work permanently in the country she/he will visit if not applying for an 
immigrant or work visa respectively
·         fails to demonstrate intent to return (for non-immigrants)
·         fails to provide sufficient evidence/documents to prove eligibility for the visa sought after
·         does not have a legitimate reason for the journey
·         has no visible means of sustenance
·         does not have travel arrangements (i.e. transport and lodging) in the destination country
·         does not have health/travel insurance valid for the destination and the duration of stay
·         has a sexually transmitted disease
·         is applying on excessively short notice without an exceptionally justifiable reason
·         is a citizen of a country to which the destination country is hostile
·         has previously visited, or intends to visit, a country to which the destination country is hostile
·         has a communicable disease, such as tuberculosis
·         has a passport that expires too soon
·         didn't use a previously issued visa at all without a valid reason (e.g., a trip cancellation due to a 
family emergency)
Even if a traveler does not need a visa, the aforementioned criteria can also be used by border police to refuse the traveler entry into the country in question.
</p>


<h5>Visa policies</h5>
<p>
The main reasons states impose visa restrictions on foreign nationals are to curb illegal immigration, security concerns, and reciprocity for visa restrictions imposed on their own nationals. Typically, nations impose visa restrictions on citizens of poorer countries, along with politically unstable and undemocratic ones, as it is likely that people from these countries will seek to illegally immigrate. Visa restrictions may also be imposed when nationals of another country are perceived as likelier to be terrorists or criminals, or by autocratic regimes that perceive foreign influence to be a threat to their rule. According to Professor Eric Neumayer of the London School of Economics:
"The poorer, the less democratic and the more exposed to armed political conflict the target country is, the more likely that visa restrictions are in place against its passport holders. The same is true for countries whose nationals have been major perpetrators of terrorist acts in the past".
Some countries apply the principle of reciprocity in their visa policy. A country's visa policy is called 'reciprocal' if it imposes visa requirements against citizens of all the countries which impose visa requirements against its own citizens. The opposite is rarely true: a country rarely lifts visa requirements against citizens of all the countries which also lift visa requirements against its own citizens, unless a prior bilateral agreement has been made.
A fee may be charged for issuing a visa; these are often also reciprocal—hence, if country A charges country B's citizens US$50 for a visa, country B will often also charge the same amount for country A's visitors. The fee charged may also be at the discretion of each embassy. A similar reciprocity often applies to the duration of the visa (the period in which one is permitted to request entry of the country) and the number of entries one can attempt with the visa. Other restrictions, such as requiring fingerprints and photographs, may also be reciprocated. Expedited processing of the visa application for some countries will generally incur additional charges.
Government authorities usually impose administrative entry restrictions on foreign citizens in three ways - countries whose nationals may enter without a visa, countries whose nationals may obtain a visa on arrival and countries whose nationals require a visa in advance. Nationals who require a visa in advance are usually advised to obtain them at a diplomatic mission of their destination country. Several countries allow nationals of countries that require a visa to obtain them online.
