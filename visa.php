        <h5>What is a Tourist Visa?</h5>
        <p>
        A tourist visa is a stamp or document needed so you can stay in Thailand for a specific period of time, 
        for the purpose of tourism. It is stamped on your passport upon arrival at the airport in Thailand, 
        or it can be obtained in your home country through the Thai embassy or consulate located there. 
        The tourist visa can also be extended if necessary. 
        There are certain countries that made agreements with the government of Thailand so their citizens can benefit from the visa 
        exemption when entering Thailand as tourists.</p>

        <h5>Travel Visa</h5>
        <p>
        A visa (from the Latin charta visa, meaning "paper which has been seen")[1] is a conditional authorization granted by a country to a foreigner, allowing them to enter, remain within, or to leave that country. Visas typically include limits on the duration of the foreigner's stay, territory within the country they may enter, the dates they may enter, the number of permitted visits or an individual's right to work in the country in question. Visas are associated with the request for permission to enter a country and thus are, in some countries, distinct from actual formal permission for an alien to enter and remain in the country. In each instance, a visa is subject to entry permission by an immigration official at the time of actual entry, and can be revoked at any time.
A visa most commonly takes the form of a sticker endorsed in the applicant's passport or other travel document. The visa, when required, was historically granted by an immigration official on a visitor's arrival at the frontiers of a country, but increasingly today a traveler wishing to enter another country must apply in advance for a visa, sometimes in person at a consular office, by mail or over the internet. The actual visa may still be a sticker or a stamp in the passport, or may take the form of a separate document or an electronic record of the authorization, which the applicant can print before leaving home and produce on entry to the host country. Some countries do not require visas for short visits.
Some countries require that their citizens, as well as foreign travelers, obtain an "exit visa" to be allowed to leave the country. Uniquely, the Norwegian special territory of Svalbard is an entirely visa-free zone under the terms of the Svalbard Treaty.
Some countries—such as those in the Schengen Area—have agreements with other countries allowing each other's citizens to travel between them without visas. The World Tourism Organization announced that the number of tourists requiring a visa before traveling was at its lowest level ever in 2015
A visa generally gives non-citizens clearance to enter a country and to remain there within specified constraints, such as a time frame for entry, a limit on the time spent in the country, and a prohibition against employment. Many countries do not require a visa in some situations; this may be the result of treaties specifying reciprocal arrangements. The possession of a visa is not in itself a guarantee of entry into the country that issued it, and a visa can be revoked at any time.
A visa application in advance of arrival gives the country a chance to consider the applicant's circumstances, such as financial security, reason for traveling, and details of previous visits to the country. A visitor may also be required to undergo and pass security or health checks upon arrival at the border.
</p>

<h5>Conditions of Issue</h5>
<p>
Some visas can be granted on arrival or by prior application at the country's embassy or consulate, or through a private visa service specialist who is specialized in the issuance of international travel documents. These agencies are authorized by the foreign authority, embassy, or consulate to represent international travelers who are unable or unwilling to travel to the embassy and apply in person. Private visa and passport services collect an additional fee for verifying customer applications, supporting documents, and submitting them to the appropriate authority. If there is no embassy or consulate in one's home country, then one would have to travel to a third country (or apply by post) and try to get a visa issued there. Alternatively, in such cases visas may be pre-arranged for collection on arrival at the border. The need or absence of need of a visa generally depends on the citizenship of the applicant, the intended duration of the stay, and the activities that the applicant may wish to undertake in the country he visits; these may delineate different formal categories of visas, with different issue conditions.
 
 The issuing authority, usually a branch of the country's foreign ministry or department (e.g. U.S. State Department), and typically consular affairs officers, may request appropriate documentation from the applicant. This may include proof that the applicant is able to support himself in the host country (lodging, food), proof that the person hosting the applicant in his or her home really exists and has sufficient room for hosting the applicant, proof that the applicant has obtained health and evacuation insurance, etc. Some countries ask for proof of health status, especially for long-term visas; some countries deny such visas to persons with certain illnesses, such as AIDS. The exact conditions depend on the country and category of visa. Notable examples of countries requiring HIV tests of long-term residents are Russia[6] and Uzbekistan.[7] However, in Uzbekistan, the HIV test requirement is sometimes not strictly enforced.[7] Other countries require a medical test that includes an HIV test, even for a short-term tourism visa. For example, Cuban citizens and international exchange students require such a test approved by a medical authority to enter Chilean territory.
 The issuing authority may also require applicants to attest that they have had no criminal convictions, or that they do not partake in certain activities (like prostitution or drug trafficking). Some countries will deny visas if the travelers' passports show evidence of citizenship of, or travel to, a country which is considered hostile by that country. For example, some Arabic-oriented countries will not issue visas to nationals of Israel and those whose passports bear evidence of visiting Israel.
 Many countries frequently demand strong evidence of intent to return to the home country, if the visa is for a temporary stay, due to potential unwanted illegal immigration.
</p>

