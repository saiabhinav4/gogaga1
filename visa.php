</p>

<h5>Visa exemption agreements</h5>
<p>
Possession of a valid visa is a condition for entry into many countries, and exemption schemes exist. In some cases visa-free entry may be granted to holders of diplomatic passports even as visas are required by normal passport holders (see: Passport).
Some countries have reciprocal agreements such that a visa is not needed under certain conditions, e.g., when the visit is for tourism and for a relatively short period. Such reciprocal agreements may stem from common membership in international organizations or a shared heritage:
·         All citizens of European Union and EFTA member countries can travel to and stay in all other EU 
and EFTA countries without a visa. See Four Freedoms (European Union) and Citizenship of the 
               European Union. Also See Brexit.
·         The United States Visa Waiver Program allows citizens of 38 countries to travel to the United 
        States without a visa (although a pre-trip entry permission, ESTA, is needed).
·         Any Gulf Cooperation Council (GCC) citizen can enter and stay as long as required in any other 
GCC member state.
·         All citizens of members of the Economic Community of West African States (ECOWAS), 
excluding those defined by law as undesirable aliens, may enter and stay without a visa in any 
member state for a maximum period of 90 days. The only requirement is a valid travel 
document and international vaccination certificates.
·         Nationals of the East African Community member states do not need visas for entry into any of 
the member states.
·         Some countries in the Commonwealth do not require tourist visas of citizens of other 
Commonwealth countries.
·         Citizens of member states of the Association of Southeast Asian Nations do not require tourist 
visas to visit another member state, with the exception of Myanmar. Until 2009, Burmese citizens 
were required to have visas to enter all other ASEAN countries. Following the implementation of 
visa exemption agreements with the other ASEAN countries, in 2016 Burmese citizens are only 
required to have visas to enter Malaysia and Singapore. Myanmar and Singapore had agreed 
on a visa exemption scheme set to be implemented on 1 December 2016. ASEAN citizens are 
entitled to use the Burmese visa on arrival.
·         Commonwealth of Independent States (CIS) member states mutually allow their citizens to 
enter visa-free, at least for short stays. There are exceptions between Tajikistan and Uzbekistan, 
and between Armenia and Azerbaijan.
·         Nepal and India allow their citizens to enter, live and work in each other's countries due to the 
Indo-Nepal friendship treaty of 1951. Indians do not require a visa or passport to travel to Bhutan 
and are only required to obtain passes at the border checkpoints, whilst Bhutan nationals 
holding a valid Bhutanese passport are authorized to enter India without a visa.
·         In the past, Qatar citizens did not require permission to enter the UAE, Saudi Arabia and other 
Gulf nations. However, that has changed.
·         Sometimes, citizens who hold a valid visa for America or another country, can enter another 
country.
Other countries may unilaterally grant visa-free entry to nationals of certain countries to facilitate tourism, promote business, or even to cut expenses on maintaining consular posts abroad.
Some of the considerations for a country to grant visa-free entry to another country include (but are not limited to):
·         being a low security risk for the country potentially granting visa-free entry
·         diplomatic relationship between two countries
·         conditions in the visitor's home country as compared to the host country
·         having a low risk of overstaying or violating visa terms in the country potentially granting 
visa-free entry
To have a smaller worldwide diplomatic staff, some countries rely on other countries (or countries') judgments when issuing visas. For example, Mexico allows citizens of all countries to enter without Mexican visas if they possess a valid American visa that has already been used. Costa Rica accepts valid visas of Schengen/EU countries, Canada, Japan, South Korea and the United States (if valid for at least 3 months on date of arrival). The ultimate example of such reliance is Andorra which imposes no visa requirements of its own because it has no international airport and is inaccessible by land without passing through the territory of either France or Spain and is thus "protected" by the Schengen visa system.
Visa-free travel between countries also occurs in all cases where passports (or passport-replacing documents such as laissez-passer) are not needed for such travel. (For examples of passport-free travel, see International travel without passports.)
As of 2016, the Visa Restrictions Index ranks the German passport as the one with the most visa exemptions by other nations, allowing holders of this passport to visit 177 countries without obtaining a visa in advance of arrival.
Common visas
Normally, visas are valid for entry only into the country which issued the visa. Countries that are members of regional organizations or party to regional agreements may however issue visas valid for entry into some or all of the member states of the organization or agreement:
·         The Schengen Visa is a visa for the Schengen Area, which consists of the European Economic Area, plus several other adjacent countries. The visa allows visitors to stay in the Schengen Area for up to 90 days within a 180-day period. The visa is valid for tourism, family visits, and business.
·         The Central American Single Visa (Visa Única Centroamericana) is a visa for Guatemala, El Salvador, Honduras, and Nicaragua. It was implemented by the CA-4 agreement. It allows citizens of those four countries free access to other member countries. It also allows visitors to any member country to enter another member country without having to obtain another visa.</p>


<h5>Possible common visa schemes</h5>
<p>
These are potentially new common visas:
·         The <strong>ASEAN common visa </strong>scheme is considered when the labor union goes into effect in 2016 (being delayed a year); however, Thailand, Cambodia, Vietnam, Laos, and Myanmar are opting in earlier. After talk arose of a CLMV common visa, with Thailand being omitted, Thailand initiated and begun implementation of a trial common visa with Cambodia, but cited security risks as the major hurdle. The trial run was delayed, but Thailand implemented a single visa scheme with Cambodia beginning on December 27, 2012, on a trial basis.
·         A Gulf Cooperation Council single visa has been recommended as a study submitted to the council.
·         The Pacific Alliance, which currently consists of Chile, Colombia, Mexico and Peru, offer a common visa for tourism purposes only in order to make it easier for nationals from countries outside of the alliance to travel through these countries by not having to apply for multiple visas.
·         An <strong>East African Single Tourist Visa</strong> is under consideration by the relevant sectoral authorities under the East African Community (EAC) integration program. If approved the visa will be valid for all five partner states in the EAC (Kenya, Tanzania, Uganda, Rwanda and Burundi). Under the proposal for the visa, any new East African single visa can be issued by any partner state's embassy. The visa proposal followed an appeal by the tourist boards of the partner states for a common visa to accelerate promotion of the region as a single tourist destination and the EAC Secretariat wants it approved before November's World Travel Fair (or World Travel Market) in London. When approved by the East African council of ministers, tourists could apply for one country's entry visa which would then be applicable in all regional member states as a single entry requirement initiative. This is also considered by COMESA.
·         The <strong>SADC UNIVISA</strong> (or Univisa) has been in development since Southern African Development Community (SADC) members signed a Protocol on the Development of Tourism in 1998. The Protocol outlined the Univisa as an objective so as to enable the international and regional entry and travel of visitors to occur as smoothly as possible. It was expected to become operational by the end of 2002. Its introduction was delayed and a new implementation date, the end of 2006, was announced. The univisa was originally intended to only be available, initially, to visitors from selected "source markets" including Australia, the Benelux countries, France, Germany, Italy, Japan, Portugal, Spain, the United Kingdom and the United States. It is now expected that when the Univisa is implemented, it will apply to non-SADC international (long-haul) tourists traveling to and within the region and that it will encourage multi - destination travel within the region. It is also anticipated that the univisa will enlarge the tourist market for transfrontier parks by lowering the boundaries between neighboring countries in the parks. The visa is expected to be valid for all the countries with trans frontier parks (Botswana, Lesotho, Mozambique, Namibia, South Africa and Zimbabwe) and some other SADC countries (Angola and Swaziland). As of 2017, universal visa is implemented by Zambia and Zimbabwe. Nationals of 65 countries and territories are eligible for visa on arrival that is valid for both countries. This visa is branded KAZA Uni-visa programmeafter Kavango–Zambezi Transfrontier Conservation Area (KAZA). It is expected that other SADC countries will join the programme in the future.


<h5>Previous common visa schemes</h5>
<p>
These schemes no longer operate.
·         The CARICOM Visa was introduced in late 2006 and allowed visitors to travel between 10 CARICOM member states (Antigua & Barbuda, Barbados, Dominica, Grenada, Guyana, Jamaica, St. Kitts & Nevis, St. Lucia, St. Vincent & the Grenadines and Trinidad and Tobago). These ten member countries had agreed to form a "Single Domestic Space" in which travelers would only have their passport stamped and have to submit completed, standardized entry and departure forms at the first port and country of entry. The CARICOM Visa was applicable to the nationals of all countries except CARICOM member states (other than Haiti) and associate member states, Canada, France, Germany, Ireland, Italy, Japan, the Netherlands, South Africa, the United Kingdom, the United States of America and the overseas countries, territories or departments of these countries. The CARICOM Visa could be obtained from the Embassies/Consulates of Barbados, Jamaica and Trinidad & Tobago and in countries which have no CARICOM representatives, the applications forms could be obtained from the embassies and consulates of the United Kingdom. The common visa was only intended for the duration of the 2007 Cricket World Cup and was discontinued on May 15, 2007. However, discussions are ongoing into instituting a revised CARICOM visa on a permanent basis in the future.

·         A predecessor of the Schengen common visa was the Benelux visa. Visas issued by Belgium, Netherlands and Luxembourg were valid for all the three countries.
</p>

<h6>Exit visas</h6>

<h5>Europe</h5>
<p>
During the Fascist period in Italy, an exit visa was required from 1922 to 1943. Nazi Germany required exit visas from 1933 to 1945. The Soviet Union and its Warsaw Pact allies required exit visas both for emigration and for those who wanted to leave the USSR for a shorter period.
Some countries, including the Czech Republic, require that an alien who needs a visa on entry be in possession of a valid visa upon exit. To satisfy this formal requirement, exit visas sometimes need to be issued. Russia requires an exit visa if a visitor stays past the expiration date of their visa. They must then extend their visa or apply for an exit visa and are not allowed to leave the country until they show a valid visa or have a permissible excuse for overstaying their visa (e.g., a note from a doctor or a hospital explaining an illness, missed flight, lost or stolen visa). In some cases, the Ministry of Foreign Affairs can issue a Return-Home certificate that is valid for ten days from the embassy of the visitor's native country, thus eliminating the need for an exit visa.
A foreign citizen granted a temporary residence permit in Russia needs a temporary resident visa to take a trip abroad (valid for both exit and return). It is also colloquially called an exit visa. Not all foreign citizens are subject to that requirement. Citizens of Germany, for example, do not require this exit visa.
</p>


<h5>Asia</h5>
<p>
Saudi Arabia and Qatar have an exit visa requirement, particularly for foreign workers. This is part of the kafala system, also present in Lebanon, Jordan, United Arab Emirates, Iraq, Kuwait, and Oman. Consequently, at the end of a foreign worker's employment period, the worker must secure clearance from their employer stating that the worker has satisfactorily fulfilled the terms of their employment contract or that the worker's services are no longer needed. The exit visa can also be withheld if there are pending court charges that need to be settled or penalties that have to be meted out.
Nepal requires citizens emigrating to the United States on an H-1B visa to present an exit permit issued by the Ministry of Labour. This document is called a work permit and needs to be presented to immigration to leave the country.
Uzbekistan is the last remaining former USSR state that still requires an exit visa, which is valid for a two-year period. There have been explicit UN complaints about this practice.
The DPRK (North Korea) requires that its citizens obtain an exit visa stating the traveler's destination country and time to be spent abroad before leaving the country. Additionally, DPRK authorities also require that their citizens obtain a re-entry visa from a DPRK embassy or mission abroad before being allowed back into the DPRK.
The government of the People's Republic of China requires its citizens to obtain a Two-way Permit, issued by the PRC authorities, prior to their visit to the Chinese dependencies of Hong Kong or Macau. The Two-way Permit is a de facto exit visa for Hong Kong- or Macau-bound trips for PRC citizens.
Singapore operates an Exit Permit scheme in order to enforce the national service obligations of its male citizens and permanent residents. Requirements vary according to age and status:
